// Combine all chillers
const allChillers = [
  ...carrierChillers,
  ...traneChillers,
  ...yorkChillers,
  ...daikinChillers,
];

Deno.serve(async (req) => {
  // Handle CORS preflight
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseUrl = Deno.env.get("SUPABASE_URL");
    const supabaseServiceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY");

    if (!supabaseUrl || !supabaseServiceKey) {
      throw new Error("Missing Supabase environment variables");
    }

    const supabase = createClient(supabaseUrl, supabaseServiceKey);

    // Check for force parameter from request body
    const body = await req.json().catch(() => ({}));
    const force = body.force === true;

    // Check if chillers already exist
    const { count, error: countError } = await supabase
      .from("equipment_catalog")
      .select("*", { count: "exact", head: true })
      .eq("equipment_category", "chiller");

    if (countError) {
      throw countError;
    }

    if (count && count > 0 && !force) {
      return new Response(
        JSON.stringify({
          seeded: false,
          message: `Chiller catalog already contains ${count} entries. Use ?force=true to reseed.`,
          existingCount: count,
        }),
        {
          headers: { ...corsHeaders, "Content-Type": "application/json" },
          status: 200,
        }
      );
    }

    // If force, delete existing chillers first
    if (force && count && count > 0) {
      const { error: deleteError } = await supabase
        .from("equipment_catalog")
        .delete()
        .eq("equipment_category", "chiller");

      if (deleteError) {
        throw deleteError;
      }
    }

    // Insert all chillers
    const { data, error: insertError } = await supabase
      .from("equipment_catalog")
      .insert(allChillers)
      .select();

    if (insertError) {
      throw insertError;
    }

    // Calculate counts by manufacturer
    const byManufacturer = allChillers.reduce((acc, chiller) => {
      acc[chiller.manufacturer] = (acc[chiller.manufacturer] || 0) + 1;
      return acc;
    }, {} as Record<string, number>);

    // Calculate counts by type
    const byType = allChillers.reduce((acc, chiller) => {
      acc[chiller.equipment_subcategory] = (acc[chiller.equipment_subcategory] || 0) + 1;
      return acc;
    }, {} as Record<string, number>);

    return new Response(
      JSON.stringify({
        seeded: true,
        count: data?.length || 0,
        byManufacturer,
        byType,
        message: `Successfully seeded ${data?.length || 0} AHRI-certified chiller models`,
      }),
      {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 200,
      }
    );
  } catch (error: unknown) {
    console.error("Error seeding chiller catalog:", error);
    const errorMessage = error instanceof Error ? error.message : "Failed to seed chiller catalog";
    return new Response(
      JSON.stringify({
        error: errorMessage,
      }),
      {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 500,
      }
    );
  }
});
