// Helper to calculate CHW flow in L/s from tons and delta-T
const calcFlowLps = (tons: number, deltaT: number): number => {
  // GPM = (tons * 24) / deltaT, then convert to L/s
  const gpm = (tons * 24) / deltaT;
  return Math.round(gpm * 0.0631 * 10) / 10;
};

